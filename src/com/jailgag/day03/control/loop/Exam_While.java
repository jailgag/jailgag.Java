package com.jailgag.day03.control.loop;

public class Exam_While {
	public static void main(String[] args) {
		/*
		 * 제어문 -반복문
		 * 2.While
		 * 문법
		 * while(조건식) { 실행문장; }
		 * while를 쓸떄는
		 * 
		 * for문을 쓸때는 (초기식; 조건식;증감식 쓰고 실행문장을 쓰지만...
		 * 
		 * for문으로 1부터 10까지 출력해보세요
		 */
		int i= 1;
		while(i<=10) {
		System.out.println(i);
		i++;
		}
	}
}
